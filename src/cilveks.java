
public class cilveks {
	String vards;
	String uzvards;
	String talrunis;
	String majasAdrese;
	String veids;
	
	//konstruktors
	public cilveks(String vards, String uzvards, String talrunis, String majasAdrese, String veids) {
		this.vards = vards;
		this.uzvards = uzvards;
		this.talrunis = talrunis;
		this.majasAdrese = majasAdrese;
		this.veids = veids;
	}
	
	//set metodes
		public void setVards(String vards) {
			this.vards=vards;
		}
		public void setUzvards(String uzvards) {
			this.uzvards = uzvards;
		}
		public void setTalrunis(String talrunis) {
			this.talrunis = talrunis;
		}
		public void setMajasAdrese(String majasAdrese) {
			this.majasAdrese = majasAdrese;
		}
		public void setVeids(String veids) {
			this.veids = veids;
		}
		
	//get metodes
		
		public void getVards(String vards) {
			this.vards=vards;
		}
		public void getUzvards(String uzvards) {
			this.uzvards = uzvards;
		}
		public void getTalrunis(String talrunis) {
			this.talrunis = talrunis;
		}
		public void getMajasAdrese(String majasAdrese) {
			this.majasAdrese = majasAdrese;
		}
		public void getVeids(String veids) {
			this.veids = veids;
		}
		
}
